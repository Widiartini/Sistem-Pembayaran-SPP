    private void bcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcariActionPerformed
       try{
           Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_login","root","");
           Statement statement = koneksi.createStatement();
           ResultSet res = statement.executeQuery("SELECT * from datamhs WHERE "+"nim like'%"+txtcari.getText()+"%' OR "+"nama like'%"+txtcari.getText()+"%'");
           DefaultTableModel tbl = new DefaultTableModel();
           tbl.addColumn("NIM");
           tbl.addColumn("Nama Mahasiswa");
           tbl.addColumn("Jurusan");
           tbl.addColumn("Jenis Kelamin");
           tbl.addColumn("No tlp");
           tbl.addColumn("Alamat");
           
           tabel.setModel(tbl);
           while(res.next()){
               tbl.addRow(new Object []{
                   res.getString("nim"),
                   res.getString("nama"),
                   res.getString("jurusan"),
                   res.getString("jenis_kelamin"),
                   res.getString("no_tlp"),
                   res.getString("alamat"),
               });
               tabel.setModel(tbl);
               txtnim.setText(res.getString("nim"));
               txtnama.setText(res.getString("nama"));
               txtjurusan.setText(res.getString("jurusan"));
               cbjk.setSelectedItem(res.getString("jenis_kelamin"));
               txtnotlp.setText(res.getString("no_tlp"));
               txtalamat.setText(res.getString("alamat"));
           }
       } catch (Exception e) {
           JOptionPane.showMessageDialog(rootPane, e.getMessage().toString());
        }
    }//GEN-LAST:event_bcariActionPerformed

