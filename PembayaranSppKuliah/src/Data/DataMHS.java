    private void cbjenjangItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbjenjangItemStateChanged
        // TODO add your handling code here:
        Object item = evt.getItem();

        if (evt.getStateChange() == evt.SELECTED) {
            try{
                Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_login","root","");
                Statement statement = koneksi.createStatement();
                ResultSet res = statement.executeQuery("SELECT A.* FROM `master_ukt` A WHERE A.nama_ukt = '"+item+"';");
               
                while(res.next()){
                    label_master_ukt.setText(res.getString("id_master_ukt"));
                    txtnominal.setText(res.getString("biaya"));
                }
                
            }catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage().toString());
            }
        } 
    }//GEN-LAST:event_cbjenjangItemStateChanged

